#include <iostream>
#include <cassert>

using namespace std;

	int main() {
	
		int a,b;
		a = 3;
		b = 3;
		assert (a == b);
		
		
		bool c,d;
		c = true;
		d = true;
		assert (c == d);
		
		
		double e,f;
		e = 2.0;
		f = 2.0;
		assert (e == f);
		
		
		char g,h;
		g = 'A';
		h = 'A';
		assert (g == h);
		
		unsigned i,j;
		i = 20;
		j = 20;
		assert (i == j);
		
		
	   	string l,m;
		l = "hola";
		m = "hola";
		assert (l == m);
	
	}
		
